- (void)testThatInitializationWorks
{
    NSDictionary *sourceDictionary = @{@"id": @"8675309",
                                       @"caption": @"",
                                       @"number_of_likes": @"0"};
    
    Media *testMedia = [[Media alloc] initWithDictionary:sourceDictionary];
    
    XCTAssertEqualObjects(testMedia.idNumber, sourceDictionary[@"id"], @"The ID number should be equal.");
    XCTAssertEqualObjects(testMedia.caption, sourceDictionary[@"caption"], @"The caption should be equal.");
    XCTAssertEqual(testMedia.numberOfLikes, [sourceDictionary[@"number_of_likes"] integerValue], @"The number of likes should be equal");
    
}

